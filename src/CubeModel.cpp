/**
 *	Attribute変数の設定
 */
// position
void CubeModel::SetAttrPos(Shader *pShader, const std::string &name) {
    assert(pShader != nullptr);
    // 位置属性のセット
    if(pShader)
        pShader->SetAttribf(name, 3, GL_FALSE, 6, gCubeVertexData);
}

void CubeModel::SetAttrNorm(Shader *pShader, const std::string &name) {
    assert(pShader != nullptr);
    // 法線属性のセット
    if(pShader)
        pShader->SetAttribf(name, 3, GL_FALSE, 6, gCubeVertexData+3);
}

